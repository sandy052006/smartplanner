// Shorthand getter
function t(key) {
  const lang = document.documentElement.getAttribute('data-lang') || 'en';
  const dict = TRANSLATIONS[lang] || TRANSLATIONS['en'];
  return dict[key] || TRANSLATIONS['en'][key] || key;
}

// Apply translations to all [data-i18n] elements
function applyTranslations() {
  const lang = document.documentElement.getAttribute('data-lang') || 'en';
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    const val = t(key);
    if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
      el.placeholder = val;
    } else {
      el.textContent = val;
    }
  });
  // Set RTL for Arabic
  document.documentElement.setAttribute('dir', lang === 'ar' ? 'rtl' : 'ltr');
}

// Theme application
function applyTheme(theme, customColors = {}) {
  const root = document.documentElement;
  root.setAttribute('data-theme', theme);

  if (theme === 'custom') {
    root.style.setProperty('--primary', customColors.primary || '#6366f1');
    root.style.setProperty('--bg', customColors.bg || '#0f172a');
    root.style.setProperty('--surface', customColors.surface || '#1e293b');
    root.style.setProperty('--text', customColors.text || '#f8fafc');
    root.style.setProperty('--accent', customColors.accent || '#10b981');
  }
}

// Init on DOM ready
document.addEventListener('DOMContentLoaded', () => {
  // Apply saved language
  const savedLang = localStorage.getItem('sp_lang') ||
    document.documentElement.getAttribute('data-lang') || 'en';
  document.documentElement.setAttribute('data-lang', savedLang);
  applyTranslations();

  // Language switcher
  const langSelect = document.getElementById('lang-switcher');
  if (langSelect) {
    langSelect.value = savedLang;
    langSelect.addEventListener('change', (e) => {
      const lang = e.target.value;
      localStorage.setItem('sp_lang', lang);
      document.documentElement.setAttribute('data-lang', lang);
      applyTranslations();
    });
  }

  // Theme buttons
  document.querySelectorAll('[data-theme-btn]').forEach(btn => {
    btn.addEventListener('click', () => {
      const theme = btn.getAttribute('data-theme-btn');
      localStorage.setItem('sp_theme', theme);
      const customColors = {
        primary: localStorage.getItem('sp_color_primary') || '#6366f1',
        bg: localStorage.getItem('sp_color_bg') || '#0f172a',
        surface: localStorage.getItem('sp_color_surface') || '#1e293b',
        text: localStorage.getItem('sp_color_text') || '#f8fafc',
        accent: localStorage.getItem('sp_color_accent') || '#10b981',
      };
      applyTheme(theme, customColors);
    });
  });

  // Color pickers
  document.querySelectorAll('.color-picker').forEach(picker => {
    picker.addEventListener('input', (e) => {
      const prop = e.target.getAttribute('data-color-var');
      if (prop) {
        localStorage.setItem(`sp_color_${prop}`, e.target.value);
        document.documentElement.style.setProperty(`--${prop}`, e.target.value);
      }
    });
  });
});
